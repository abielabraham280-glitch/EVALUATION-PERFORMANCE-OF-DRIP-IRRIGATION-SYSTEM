import streamlit as st
import numpy as np

# Function to calculate Distribution Uniformity (if needed)
def calculate_distribution_uniformity(discharge_values, avg_discharge):
    # Simple calculation example, please replace with your actual calculation
    return np.round(np.mean(discharge_values) / avg_discharge * 100, 2)

# Function for Efficiency Uniformity
def calculate_uniformity_efficiency(DU):
    # Replace this with your accepted efficiency calculation
    return DU >= 85  # Example: return True if DU is greater than or equal to 85%

# Streamlit App
def main():
    st.title("Pressurized Irrigation Uniformity and Efficiency Calculator")

    st.sidebar.header("Input Parameters")
    num_cans = st.sidebar.number_input("Number of Cans:", min_value=1, value=5)

    discharge_values = []
    for i in range(num_cans):
        discharge = st.sidebar.number_input(f"Discharge for Can {i + 1} (L/h): ", value=10)
        discharge_values.append(discharge)

    avg_discharge = st.sidebar.number_input("Average Discharge (L/h): ", value=10)
    distribution_uniformity = st.sidebar.number_input("Distribution Uniformity (%): ", value=75)

    # Calculate metrics based on provided inputs
    if st.sidebar.button("Calculate"):
        DU = calculate_distribution_uniformity(discharge_values, avg_discharge)
        efficiency = calculate_uniformity_efficiency(DU)

        # Display results
        st.subheader("Results")
        st.write(f"Calculated Distribution Uniformity: {DU}%")
        if efficiency:
            st.success("Uniformity Efficiency is Acceptable!")
        else:
            st.warning("Uniformity Efficiency is Not Acceptable.")

if __name__ == "__main__":
    main()